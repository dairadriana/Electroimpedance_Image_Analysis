def calculate_stats(data_list):
    n = len(data_list)
    if n == 0: return 0.0, 0.0
    mean = sum(data_list) / n
    variance = sum((x - mean) ** 2 for x in data_list) / (n - 1)
    std = math.sqrt(variance)
    return mean, std

